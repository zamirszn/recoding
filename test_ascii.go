func unknownGlyph() []string {
	return []string{
		` ___ `,
		`|__ \`,
		`  ) |`,
		` / / `,
		`/ /_ `,
		`|____|`,
	}
}

func render(text string) string {
	// Collect glyphs — pad any short glyphs to numRows
	var rows [numRows]strings.Builder

	for _, ch := range text {
		glyph, ok := font[ch]
		if !ok {
			glyph = unknownGlyph()
		}

		// Pad glyph height if needed
		maxW := 0
		for _, row := range glyph {
			if len(row) > maxW {
				maxW = len(row)
			}
		}
		for len(glyph) < numRows {
			glyph = append(glyph, strings.Repeat(" ", maxW))
		}

		// Recompute maxW (in case padding changed things)
		maxW = 0
		for _, row := range glyph {
			if len(row) > maxW {
				maxW = len(row)
			}
		}

		for i := 0; i < numRows; i++ {
			line := glyph[i]
			if len(line) < maxW {
				line += strings.Repeat(" ", maxW-len(line))
			}
			rows[i].WriteString(line)
			rows[i].WriteString(" ") // 1-space gap between letters
		}
	}

	var out strings.Builder
	for _, row := range rows {
		line := strings.TrimRight(row.String(), " ")
		out.WriteString(line)
		out.WriteString("\n")
	}
	return out.String()
}

func main() {
	if len(os.Args) < 2 {
		fmt.Fprintln(os.Stderr, "usage: go run . \"<text>\"")
		os.Exit(1)
	}
	text := strings.Join(os.Args[1:], " ")
	fmt.Print(render(text))
}
